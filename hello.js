  let fruits = ["Apple", "Banana", "Kiwi"]

  // To get the first value of the array
  document.write("First item of Fruits Array = " + fruits[0] + "<br>");

  // To Push & Pop the array
  fruits.push("Orange"); // adds 6 to the end of the array
  document.write("Fruits Array  = " + fruits + "<br>");
  fruits.pop(); // removes the last element (Orange) from the array
  document.write("Fruits Array after Pop  = " + fruits + "<br>");